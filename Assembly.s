# Preconditions:
#   1st parameter (a0) address of the first element of the dimension info (address of asize[0])
#   2nd parameter (a1) address of the first element of the frame array (address of frame[0][0])
#   3rd parameter (a2) address of the first element of the window array (address of window[0][0])
# Postconditions:	
#   result (v0) x coordinate of the block in the frame with the minimum SAD
#          (v1) y coordinate of the block in the frame with the minimum SAD


# Begin subroutine
vbsme:  
	
#------------------------------------------------------------REGISTER DEFINITIONS-------------------------------------------------
# s0 is a ptr to the current element in Frame 
# s1 is the amount of bytes needed to move up or down a row.			(s0 + s1) = [row+1][col] 		: 		(s0 - s1) = [row-1][col]
#	 s1 = 4 * (# of Columns)
# s2 - 
# s3 - 
# s4 - holds the address Frame[maxRowIndex][maxColIndex]	
# s5 - maxRowIndex is the bottom most row that can be traversed to
#	   s5 = FrameRowLen - WindowRowLen
# s6 - maxColIndex is the right  most column that can be traversed to
#	   s6 = FrameColLen - WindowColLen
# s7 - minSadValue

# WILL NEED TO USE $t REGISTERS TO SAVE CURRENT INDICES AND SAD VALUE
# t5 - curRowIndex
# t6 - curColIndex
# t7 - tempSadValue
# t8 - sadRowIndex
# t9 - sadColIndex

#-------------------------------------------------------END OF: REGISTER DEFINITIONS-----------------------------------------------
		 
	
	
#-----------------------------------------------------------INITIALIZATION---------------------------------------------------------

	addi	$sp,	$sp,	-4		# Make room on the Stack, Stacks grow up, LIFO
	sw		$ra,	0($sp)			# store ra on the Stack
	addi	$s7,	$s7		32767
	addi	$s0,	$a1,	0		# s0 now holds the address of Frame[0][0]
	
	lw		$t0,	0($a0)			# t0 = i,	# of Rows in the Frame
	lw		$t1,	4($a0)			# t1 = j,	# of Cols in the Frame
	lw		$t2,	8($a0)			# t2 = k,	# of Rows in the Window
	lw		$t3,	12($a0)			# t3 = l,	# of Cols in the Window
	
	sll		$s1,	$t1,	2		# s1 = (# of Columns in Frame) * 4,		to move up/down rows
	
	sub		$s5,	$t0,	$t2		# maxRowIndex, s5 = (# of Rows in Frame) - (# of Rows in Window)
	sub		$s6,	$t1,	$t3		# maxColIndex, s6 = (# of Cols in Frame) - (# of Cols in Window)
	
	# Last Element Address s4 = ((maxRowIndex * # of Cols in Frame) + maxColIndex) * 4
	mult	$s5,	$t1				# LO = (maxRowIndex) * (# of Cols in Frame)
	mflo	$s4						# s4 = LO
	
	addi	$t5,	$zero,	0		# t5 = curRowIndex = 0
	addi	$t6,	$zero,	0		# t6 = curColIndex = 0
	addi	$t8,	$zero,	0		# t8 = sadRowIndex = 0
	addi	$t9,	$zero,	0		# t9 = sadColIndex = 0
	
	add		$s4,	$s4,	$s6		# s4 = s4 + (maxColIndex)
	sll		$s4,	$s4,	2		# s4 = s4 * 4
	add		$s4,	$s4,	$s0		# s4 holds the address Frame[maxRowIndex][maxColIndex]
	


#-------------------------------------------------------ZIG ZAG STUFF------------------------------------------------------
# Check to see if at/passed the last traversable element

# Check Edge Cases
# can change blez $s, Move 		->		beq, $s, $zero
	beq		$s5,	$zero,		MoveRightUntilDone		# Edge Case: maxRowIndex == 0  blez or beq
	beq		$s6,	$zero,		MoveDownUntilDone		# Edge Case: maxColIndex == 0 

	jal		SAD								# Calculate SAD for Frame[0][0]
	
#-------------------------------------------------HORIZONTAL MOVEMENT--------------------------------------------------
# This should be the starting point of ZigZag after Calculating SAD for Frame[0][0]
# Called when MoveUpDiag moves to [0][col+1], then calls MoveDownDiag
MoveRightToMoveDownDiag:			
	addi	$s0,	$s0,	4		# s0 = [row][col+1]
	addi	$t6,	$t6,	1		# curColIndex++
	jal		SAD						# Calculate SAD value
	j		MoveDownDiag
	
# Called when MoveDownDiag moves to [maxRowIndex][col+1], then calls MoveUpDiag
MoveRightToMoveUpDiag:
	addi	$s0,	$s0,	4		# s0 = [row][col+1]
	addi	$t6,	$t6,	1		# curColIndex++
	jal		SAD						# Calculate SAD value
	j		MoveUpDiag

#----------------------------------------------VERTICAL MOVEMENT--------------------------------------------------------
# Called when MoveUpDiag moves to [row-1][maxColIndex], then calls MoveUpDiag
MoveDownToMoveDownDiag:
	add		$s0,	$s0,	$s1		# s0 = [row+1][col]
	addi	$t5,	$t5,	1		# curRowIndex++
	jal		SAD						# Calculate SAD value
	j		MoveDownDiag
	
# Called when MoveDownDiag moves to [row+1][0], then calls MoveUpDiag
MoveDownToMoveUpDiag:
	add		$s0,	$s0,	$s1		# s0 = [row+1][col]
	addi	$t5,	$t5,	1		# curRowIndex++
	jal		SAD						# Calculate SAD value
	j		MoveUpDiag

#----------------------------------------------DIAGONAL MOVEMENT--------------------------------------------------------
MoveUpDiag:
	sub		$s0,	$s0,	$s1							# s0 = [row-1][col+1]
	addi	$s0,	$s0,	4							# ADDED MOVING BY ROW AND NOT DIAGONAL NOW
	addi 	$t5,	$t5,	-1							# curRowIndex--
	addi	$t6,	$t6,	1							# curColIndex++
	jal		SAD											# Calculate SAD value
	beq		$t6,	$s6,	MoveDownToMoveDownDiag		# Move Down if curColIndex == maxColIndex
	beq		$t5,	$zero,	MoveRightToMoveDownDiag				# Move Right if curRowIndex <= 0
	j		MoveUpDiag
	

MoveDownDiag:
	add		$s0,	$s0,	$s1							# s0 = [row+1][col-1]
	addi	$s0,	$s0,	-4							# ADDED MOVING BY ROW AND NOT DIAGONAL NOW
	addi	$t5,	$t5,	1							# curRowIndex++
	addi	$t6,	$t6,	-1							# curColIndex--
	jal		SAD											# Calculate SAD value
	beq		$t5,	$s5,	MoveRightToMoveUpDiag		# If curRowIndex == maxRowIndex : Move Right
	beq 	$t6,	$zero,	MoveDownToMoveUpDiag				# If curColIndex <= 0 : Move Down 
	j 		MoveDownDiag
	
#------------------------------------------------EDGE CASES TRAVERSAL----------------------------------------------------	
MoveRightUntilDone:					# Edge Case: maxRowIndex == 0
	jal		SAD						# maybe make special SAD fnc that'll jump back to the start of this to remove the below instruction
	addi	$s0,	$s0,	4		# s0 = [row][col+1]
	addi	$t6,	$t6,	1		# curColIndex++
	j	MoveRightUntilDown			# can remove if special SAD fnc
	
MoveDownUntilDone:		
	jal		SAD						# maybe make special SAD fnc that'll jump back to this to remove the below instruction			# Edge Case: maxColIndex == 0
	add		$s0,	$s0,	$s1		# s0 = [row+1][col]
	addi	$t5,	$t5,	1		# curRowIndex++
	j	MoveDownUntilDone
#--------------------------------------------END OF: EDGE CASES TRAVERSAL-------------------------------------------------	


#--------------------------------------------------END OF: ZIG ZAG STUFF------------------------------------------------------


SAD:
#--------------Initializing SAD-------------------------------------------
	add		$t9,	$ra,	$zero	
	add		$t2,	$s0,	$zero		#sets $t2 to current element in frame.
	add		$t0,	$zero,	$a2		# stores address of window[0] in t0-------------------CHANGED
#------------------Get window Last element adress--------------------------
	lw		$t3,	8($a0)			# number of rows in window			// $t2 holds this value already
	lw		$t1,	12($a0)			# number of columns in window		// $t3 holds this value already
	sll		$t1,	$t1,	2		# multiply # of columns by 4
	mult	$t1,	$t3				# multiply rows * collumns *4------------------------------CHANGE THIS EXTRA REGISTER
	mflo	$t1						# $t1 = LO------------------------------------------------AND THIS
#------------------Start SAD Calculation------------------------------------
setup:
	add		$t1,	$t0,	$t1     # adds value of $t1 to $t0, should set $t1 to the address of last element in window
	addi	$t1,	$t1,	-4		# subtracts 4 from last element address to account for initial position
	addi	$t8,	$zero,	1		# sets t8 to 1 ---------------------------------------CHANGED MOVED TO ABOVE LW, WAS BELOW AND
	lw		$t3,	12($a0)			# loads window column # into $t3
	and		$t4,	$t4,	$zero	# sets $t4 to zero
	
	
calc:
	lw		$t7,	0($t0)			#load current window value into t7
	lw		$s3,	0($t2)			#loads current frame value into s3
	sub		$t7,	$t7,	$s3		#stores value of window value - frame value
	#-------Absolute value method--------------------------
	sra		$s3,	$t7,	31		# s3 = sign bit of t7
	xor		$t7,	$t7,	$s3		#
	sub		$t7,	$t7,	$s3		# t7 = abs value of WindowValue - FrameValue
	#-------Absolute value method end------------------------
	add		$t4,	$t4,	$t7		# t4 keeping track of the sum of absolute diffrences 
	#------end of window check---------------------------------------
#	beq		$t0,	$t1,	SADcheck #--MOVED------------------------------ADDED
	#------end of collumn check---------------------------------------
	beq		$t8,	$t3,	rowdown #---------------------------------------------------------------ADDED
	#---------------------------------------------------------------
	addi	$t0,	$t0,	4	# move window pointer to next position
	addi	$t2,	$t2,	4	# move frame ptr to next position
	addi	$t8,	$t8,	1	# increments t8 by 1
	j		calc
	
rowdown:
	beq		$t0,	$t1,	SADcheck #--MOVED------------------------------ADDED
	sll		$s3,	$t3,	2	# multiply # of columns by 4
	addi	$s3,	$s3,	-4  # subtract 4 from $s3
	sub		$t2,	$t2,	$s3	# moves $t2 back to original column position
	add		$t2,	$t2,	$s1 # move frame down by one row
	addi	$t0,	$t0,	4	# move window ptr to next position
	addi	$t8,	$zero,	1	# reset count of t8 to zero
	j 		calc
	
SADcheck:
	slt		$t0,	$t4,	$s7	#check if sad calculated is less than current sad
	beq		$t0,	1,	SADupdate
	beq		$t4,	$s7,	SADupdate #if SAD value is found equal to current sad updates quardenits
	beq		$s0,	$s4		exit	#if at last frame exits
	jr		$t9
	
SADupdate:
	add		$s7,	$t4,	$zero	#set calculated sad value to new sad vale
#***************ATTENTION******************************************************************
#v0, & v1 are being used to store the row and collumn index of current SAD value
	add		$v0,	$t5,	$zero	#set sad row to current row
	add		$v1,	$t6,	$zero	#set sad collumn to current collumn
	beq		$s0,	$s4,	exit	#if at last frame exits
	jr		$t9

#----------------WORK ON THIS MORE TOO----PUT EXIT CONDITION INSIDE SAD--------------------------
# reduces number of lines written
exit:
	lw		$ra,	0($sp)			# load the return address to the fnc that called vbsme. Saved on Stack at Initialization
	addi	$sp,	4				# Pop off the Stack
	jr		$ra					# Return to calling fnc. Exit vbsme
	
		
